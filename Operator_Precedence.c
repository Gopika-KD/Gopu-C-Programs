#include<stdio.h>
int main()
{
    // Variables a,b,c,d
    int a = 5 * 2 - 2 * 3;
    int b = 5 * 2 / 2 * 3;
    int c = 5 * (2 / 2) * 3;
    int d = 5 + 2 / 2 * 3;

    // Prints the output
    printf("The value of a = %d\n", a);
    printf("The value of b = %d\n", b);
    printf("The value of c = %d\n", c);
    printf("The value of d = %d\n", d);

    return 0;
}